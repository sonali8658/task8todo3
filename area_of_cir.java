
import java.util.*;
public class area_of_cir {
    public static void area(double r)
    {
        double area;
        double PI=3.141;
        area=2*PI*r;
        System.out.println("area of circle is="+area);
    }
    public static void main(String args[])
    {
        area(4.6);
    }
    
}
